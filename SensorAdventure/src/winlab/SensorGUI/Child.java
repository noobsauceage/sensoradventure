package winlab.SensorGUI;

public class Child {
	
	private String field;
	private String unit;
	
	
	public Child(String field, String unit){
		this.field = field;
		this.unit = unit;
		
	}
	
	public String getField(){
		return field;
	}
	
	public String getUnit(){
		return unit;
	}

}
